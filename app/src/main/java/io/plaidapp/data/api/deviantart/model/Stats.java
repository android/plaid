/**
 * Models links to the various quality of images of a Stats.
 */
public class Stats implements Parcelable {


    private final String comments;
    private final String favorites;

    public Stats(String comments, String favorites) {
        this.comments = comments;
        this.favorites = favorites;
    }
    protected Stats(Parcel in) {
        comments = in.readString();
        favorites = in.readString();
    }


    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int flags) {
        dest.writeString(comments);
        dest.writeString(favorites);
    }

    @SuppressWarnings("unused")
    public static final Parcelable.Creator<Stats> CREATOR = new Parcelable.Creator<Stats>() {
        @Override
        public Stats createFromParcel(Parcel in) {
            return new Stats(in);
        }

        @Override
        public Stats[] newArray(int size) {
            return new Stats[size];
        }
    };

    public String getLikes() {
        return favorites;
    }

    public String getComments() {
        return comments;
    }
}
